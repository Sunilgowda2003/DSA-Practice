package Butterflypatterns;

public class SmallButterfly {
    public static void main(String[] args) {
        System.out.println("Small Butterfly....");

        
        // Simple fixed pattern using print
        System.out.println("*   *");
        System.out.println("** **");
        System.out.println("*****");
        System.out.println("** **");
        System.out.println("*   *");
    }  
}

// Output:

// *   *
// ** **
// *****
// ** **
// *   *
    
